import SwiftUI

/// Extension to `Color` adding static properties for custom colors.
public extension Color {

    // MARK: Grouped Background Colors

    /// Default background color for `List` and `Form` views.
    static let groupedBackground = Color(uiColor: UIColor.systemGroupedBackground)

    /// Default background color for items in `List` and `Form` views.
    static let secondaryGroupedBackground = Color(uiColor: UIColor.secondarySystemGroupedBackground)

}
